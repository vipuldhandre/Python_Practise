
def add(a,b):
    res=a+b
    print("Sum of a={} and b={} is {}".format(a,b,res))

def sub(a,b):
    res=a-b
    print("Sub of a={} and b={} is {}".format(a,b,res))

def mul(a,b):
    res=a*b
    print("Mul of a={} and b={} is {}".format(a,b,res))

def div(a,b):
    res=a/b
    print("Div of a={} and b={} is {}".format(a,b,res))

def fdiv(a,b):
    res=a//b
    print("Fdiv of a={} and b={} is {}".format(a,b,res))

def pow(a,b):
    res=a**b
    print("Pow of a={} and b={} is {}".format(a,b,res))
        
